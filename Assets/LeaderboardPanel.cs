    static string TrimName(string name)
    {
        if (string.IsNullOrWhiteSpace(name))
        {
            return "Player";
        }
        string clean = name.Trim();
        const int max = 14;
        if (clean.Length <= max)
        {
            return clean;
        }
        return clean.Substring(0, max) + "…";
    }

