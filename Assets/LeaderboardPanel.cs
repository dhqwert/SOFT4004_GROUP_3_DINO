    void EnsureRowViews(int needed)
    {
        while (rowViews.Count < needed)
        {
            rowViews.Add(CreateRow(rowViews.Count));
        }
    }

    void EnsureBuilt()
    {
        if (isBuilt)
        {
            return;
        }
        BuildPanel();
        isBuilt = true;
    }

    void BuildPanel()
    {
        ClearChildren(transform);

        RectTransform self = (RectTransform)transform;
        self.anchorMin = Vector2.zero;
        self.anchorMax = Vector2.one;
        self.offsetMin = Vector2.zero;
        self.offsetMax = Vector2.zero;

        circleSprite = BuildCircleSprite(128);
        roundedSprite = BuildRoundedSprite(64, 16);
        if (circleSprite != null)
        {
            generatedAssets.Add(circleSprite);
            if (circleSprite.texture != null) generatedAssets.Add(circleSprite.texture);
        }
        if (roundedSprite != null)
        {
            generatedAssets.Add(roundedSprite);
            if (roundedSprite.texture != null) generatedAssets.Add(roundedSprite.texture);
        }

        AddBackgroundGradient(self);
        BuildHeaderArea(self);
        BuildTabsArea(self);
        BuildHeaderRow(self);
        BuildScrollList(self);
        BuildYouFooter(self);
    }

    void AddBackgroundGradient(RectTransform parent)
    {
        GameObject bg = new GameObject("Background", typeof(RectTransform), typeof(Image));
        bg.transform.SetParent(parent, false);
        StretchFull((RectTransform)bg.transform);

        Image img = bg.GetComponent<Image>();
        Texture2D tex = BuildGradientTexture(2, 256, backgroundTop, backgroundBottom);
        backgroundSprite = Sprite.Create(tex, new Rect(0f, 0f, tex.width, tex.height), new Vector2(0.5f, 0.5f));
        generatedAssets.Add(tex);
        generatedAssets.Add(backgroundSprite);
        img.sprite = backgroundSprite;
        img.type = Image.Type.Simple;
        img.preserveAspect = false;
        img.raycastTarget = true;
    }

    void OnDestroy()
    {
        for (int i = 0; i < generatedAssets.Count; i++)
        {
            Object asset = generatedAssets[i];
            if (asset != null) Destroy(asset);
        }
        generatedAssets.Clear();
    }

