    public void Refresh()
    {
        if (!isBuilt)
        {
            EnsureBuilt();
        }

        leagueManager = leagueManagerOverride != null ? leagueManagerOverride : FindLeagueManager();
        uiManager = uiManagerOverride != null ? uiManagerOverride : FindUIManager();
        firebaseService = firebaseServiceOverride != null ? firebaseServiceOverride : FindFirebaseService();

        UpdateTabVisuals();

        if (useFirebase && firebaseService != null && firebaseService.IsConfigured)
        {
            if (firebaseCacheRaw != null)
            {
                RenderRanking(BuildRankingFromFirebaseCache());
            }
            else
            {
                ShowStatus(loadingMessage);
                if (!isFetching)
                {
                    StartCoroutine(FetchFromFirebaseRoutine());
                }
            }
        }
        else
        {
            HideStatus();
            RenderRanking(BuildRankingForTab(activeTab));
        }
    }

    IEnumerator FetchFromFirebaseRoutine()
    {
        isFetching = true;
        bool finished = false;
        List<FirebasePlayerEntry> result = null;

        firebaseService.FetchAll(entries =>
        {
            result = entries;
            finished = true;
        });

        float waited = 0f;
        while (!finished && waited < 12f)
        {
            waited += Time.unscaledDeltaTime;
            yield return null;
        }

        isFetching = false;

        if (result == null)
        {
            ShowStatus(errorMessage);
            yield break;
        }

        // Chỉ giữ entry thuộc mùa hiện tại (auto weekly filter).
        string currentSeason = SeasonManager.GetCurrentSeasonId();
        List<FirebasePlayerEntry> filtered = new List<FirebasePlayerEntry>();
        for (int i = 0; i < result.Count; i++)
        {
            FirebasePlayerEntry e = result[i];
            if (e == null) continue;
            // Entry không có season được coi như "mùa hiện tại" để không loại nhầm seed cũ.
            if (string.IsNullOrEmpty(e.season) || e.season == currentSeason)
            {
                filtered.Add(e);
            }
        }

        ConvertFirebaseEntries(filtered);
        HideStatus();
        RenderRanking(BuildRankingFromFirebaseCache());
    }

    public void ForceReloadFromFirebase()
    {
