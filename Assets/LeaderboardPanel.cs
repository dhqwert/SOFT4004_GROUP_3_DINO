    // Cache giữ id/updatedAt để filter Daily/Weekly và xác định local player chính xác
    class CachedEntry
    {
        public string id;
        public string playerName;
        public int score;
        public long updatedAt;
    }
    List<CachedEntry> firebaseCacheRaw;

    void ConvertFirebaseEntries(List<FirebasePlayerEntry> entries)
    {
        firebaseCacheRaw = new List<CachedEntry>();
        if (entries == null) return;

        for (int i = 0; i < entries.Count; i++)
        {
            FirebasePlayerEntry e = entries[i];
            if (e == null || string.IsNullOrWhiteSpace(e.name)) continue;
            firebaseCacheRaw.Add(new CachedEntry
            {
                id = e.id,
                playerName = e.name.Trim(),
                score = Mathf.Max(0, e.score),
                updatedAt = e.updatedAt
            });
        }
    }

    List<LeaguePlayerData> BuildRankingFromFirebaseCache()
    {
        long nowUnix = System.DateTimeOffset.UtcNow.ToUnixTimeSeconds();
        long cutoff = activeTab == LeaderboardTab.Daily ? nowUnix - 86400L
                    : activeTab == LeaderboardTab.Weekly ? nowUnix - 86400L * 7L
                    : 0L;

        List<LeaguePlayerData> snapshot = new List<LeaguePlayerData>();
        if (firebaseCacheRaw == null) return snapshot;

        for (int i = 0; i < firebaseCacheRaw.Count; i++)
        {
            CachedEntry e = firebaseCacheRaw[i];
            // Daily/Weekly: chỉ giữ entry có updatedAt mới đủ. AllTime: lấy hết.
            // Entry có updatedAt = 0 (seed cũ chưa có timestamp) coi như AllTime.
            if (cutoff > 0 && e.updatedAt > 0 && e.updatedAt < cutoff) continue;
            snapshot.Add(new LeaguePlayerData
            {
                playerName = e.playerName,
                score = e.score
            });
        }
        snapshot.Sort(SortByScore);
        return snapshot;
    }

    void RenderRanking(List<LeaguePlayerData> ranking)
    {
        string localPlayerName = ResolveLocalPlayerName();

        int rowCount = Mathf.Clamp(maxRows, 1, 200);
        rowCount = Mathf.Min(rowCount, ranking.Count);
        EnsureRowViews(rowCount);

        for (int i = 0; i < rowViews.Count; i++)
        {
            LeaderboardRowView row = rowViews[i];
            bool active = i < rowCount;
            row.root.SetActive(active);
            if (!active)
            {
                continue;
            }

            int rank = i + 1;
            LeaguePlayerData player = ranking[i];
            bool isYou = string.Equals(player.playerName, localPlayerName, System.StringComparison.OrdinalIgnoreCase);

            row.background.color = isYou ? rowYouColor : (i % 2 == 0 ? rowColorOdd : rowColorEven);

            if (rank <= 3)
            {
                row.medalBadge.gameObject.SetActive(true);
                row.medalBadge.color = MedalColor(rank);
                row.rankText.text = string.Empty;
            }
            else
            {
                row.medalBadge.gameObject.SetActive(false);
                row.rankText.text = rank.ToString();
            }

            row.nameText.text = isYou ? "<b>" + TrimName(player.playerName) + "</b>" : TrimName(player.playerName);
            row.nameText.color = isYou ? youHighlight : Color.white;

            row.scoreText.text = FormatScore(player.score);
        }

        UpdateYouFooter(ranking, localPlayerName);
        UpdateTierBadge(ranking, localPlayerName);
    }

    void UpdateTierBadge(List<LeaguePlayerData> ranking, string localPlayerName)
    {
        if (tierBadgeText == null || tierBadgeBg == null) return;

        int localScore = 0;
        // Ưu tiên dùng ID Firebase (khớp 100%, không bị nhầm tên trùng) → chuẩn hơn
        // việc so sánh playerName. Fallback sang so theo tên nếu không có cache Firebase.
        if (firebaseCacheRaw != null && firebaseService != null && !string.IsNullOrEmpty(firebaseService.LocalPlayerId))
        {
            string myId = firebaseService.LocalPlayerId;
            for (int i = 0; i < firebaseCacheRaw.Count; i++)
            {
                if (firebaseCacheRaw[i].id == myId)
                {
                    localScore = firebaseCacheRaw[i].score;
                    break;
                }
            }
        }
        else
        {
            for (int i = 0; i < ranking.Count; i++)
            {
                if (string.Equals(ranking[i].playerName, localPlayerName, System.StringComparison.OrdinalIgnoreCase))
                {
                    localScore = ranking[i].score;
                    break;
                }
            }
        }

        LeagueTier tier = LeagueTierSystem.GetTierForScore(localScore);
        tierBadgeText.text = tier.displayName.ToUpperInvariant();
        tierBadgeBg.color = new Color(tier.primaryColor.r, tier.primaryColor.g, tier.primaryColor.b, 0.85f);
        tierBadgeText.color = Color.white;
    }

