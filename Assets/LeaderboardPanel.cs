    string ResolveLocalPlayerName()
    {
        if (firebaseService != null && !string.IsNullOrWhiteSpace(firebaseService.LocalPlayerName))
        {
            return firebaseService.LocalPlayerName;
        }
        return PlayerPrefs.GetString("PlayerName", localPlayerFallbackName);
    }

    void UpdateYouFooter(List<LeaguePlayerData> ranking, string localPlayerName)
    {
        int localRank = -1;
        int localScore = 0;
        for (int i = 0; i < ranking.Count; i++)
        {
            if (string.Equals(ranking[i].playerName, localPlayerName, System.StringComparison.OrdinalIgnoreCase))
            {
                localRank = i + 1;
                localScore = ranking[i].score;
                break;
            }
        }

        youFooterBackground.color = new Color(0.05f, 0.10f, 0.36f, 0.96f);
        youRankText.text = localRank > 0 ? localRank.ToString() : "-";
        youNameText.text = "<b>" + TrimName(localPlayerName) + "</b>";
        youScoreText.text = localRank > 0 ? FormatScore(localScore) : "-";
    }

    List<LeaguePlayerData> BuildRankingForTab(LeaderboardTab tab)
    {
        List<LeaguePlayerData> result;
        if (!demoCache.TryGetValue(tab, out result))
        {
            result = BuildBaseRanking(tab);
            demoCache[tab] = result;
        }
        else
        {
            UpdateLocalPlayerInList(result);
        }
        result.Sort(SortByScore);
        return result;
    }

    void UpdateLocalPlayerInList(List<LeaguePlayerData> list)
    {
        // Chỉ chạy ở fallback demo (khi Firebase OFF). Ở chế độ Firebase, entry của
        // local player được fetch trực tiếp trong list nên không cần inject thủ công.
        string localName = ResolveLocalPlayerName();
        int localScore = 0;

        for (int i = 0; i < list.Count; i++)
        {
            if (string.Equals(list[i].playerName, localName, System.StringComparison.OrdinalIgnoreCase))
            {
                return;
            }
        }
        list.Add(new LeaguePlayerData { playerName = localName, score = localScore });
    }

    List<LeaguePlayerData> BuildBaseRanking(LeaderboardTab tab)
    {
        List<LeaguePlayerData> result = new List<LeaguePlayerData>();

        if (leagueManager != null && leagueManager.localTestPlayers != null)
        {
            for (int i = 0; i < leagueManager.localTestPlayers.Length; i++)
            {
                LeaguePlayerData src = leagueManager.localTestPlayers[i];
                if (src == null || string.IsNullOrWhiteSpace(src.playerName))
                {
                    continue;
                }
                result.Add(new LeaguePlayerData
                {
                    playerName = src.playerName.Trim(),
                    score = Mathf.Max(0, src.score)
                });
            }
        }

        if (autoGenerateDemoPlayers && result.Count < demoPlayerTargetCount)
        {
            int needed = demoPlayerTargetCount - result.Count;
            int multiplier = TabScoreMultiplier(tab);
            int seed = unchecked(demoSeed + (int)tab * 7919);
            System.Random rng = new System.Random(seed);
            for (int i = 0; i < needed; i++)
            {
                string fakeName = GenerateFakeName(rng);
                int baseScore = rng.Next(150, 9000);
                result.Add(new LeaguePlayerData
                {
                    playerName = fakeName,
                    score = baseScore * multiplier
                });
            }
        }

        UpdateLocalPlayerInList(result);
        return result;
    }

    static int TabScoreMultiplier(LeaderboardTab tab)
    {
        switch (tab)
        {
            case LeaderboardTab.Daily: return 1;
            case LeaderboardTab.Weekly: return 4;
            default: return 12;
        }
    }

    static int SortByScore(LeaguePlayerData a, LeaguePlayerData b)
    {
        int byScore = b.score.CompareTo(a.score);
        if (byScore != 0)
        {
            return byScore;
        }
        return string.Compare(a.playerName, b.playerName, System.StringComparison.OrdinalIgnoreCase);
    }

    string GenerateFakeName(System.Random rng)
    {
        string[] prefixes = { "Player", "Helix", "Bouncy", "Drop", "Spin", "Jump", "Crash", "Neon", "Pixel", "Comet" };
        string prefix = prefixes[rng.Next(prefixes.Length)];
        int number = rng.Next(10, 999);
        return prefix + number;
    }

