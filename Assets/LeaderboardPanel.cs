    static Texture2D BuildGradientTexture(int width, int height, Color top, Color bottom)
    {
        Texture2D tex = new Texture2D(width, height, TextureFormat.RGBA32, false);
        tex.wrapMode = TextureWrapMode.Clamp;
        tex.filterMode = FilterMode.Bilinear;

        for (int y = 0; y < height; y++)
        {
            float t = (float)y / Mathf.Max(1, height - 1);
            Color c = Color.Lerp(bottom, top, t);
            for (int x = 0; x < width; x++)
            {
                tex.SetPixel(x, y, c);
            }
        }
        tex.Apply(false, true);
        return tex;
    }

    static Sprite BuildCircleSprite(int size)
    {
        Texture2D tex = new Texture2D(size, size, TextureFormat.RGBA32, false);
        tex.wrapMode = TextureWrapMode.Clamp;
        tex.filterMode = FilterMode.Bilinear;
        Color clear = new Color(0f, 0f, 0f, 0f);
        float radius = size * 0.5f;
        Vector2 center = new Vector2(radius, radius);
        for (int y = 0; y < size; y++)
        {
            for (int x = 0; x < size; x++)
            {
                float distance = Vector2.Distance(new Vector2(x + 0.5f, y + 0.5f), center);
                float alpha = Mathf.Clamp01(radius - distance);
                tex.SetPixel(x, y, alpha > 0f ? new Color(1f, 1f, 1f, alpha) : clear);
            }
        }
        tex.Apply(false, true);
        return Sprite.Create(tex, new Rect(0f, 0f, size, size), new Vector2(0.5f, 0.5f));
    }

    static Sprite BuildRoundedSprite(int size, int corner)
    {
        Texture2D tex = new Texture2D(size, size, TextureFormat.RGBA32, false);
        tex.wrapMode = TextureWrapMode.Clamp;
        tex.filterMode = FilterMode.Bilinear;
        Color fill = Color.white;
        Color clear = new Color(0f, 0f, 0f, 0f);

        for (int y = 0; y < size; y++)
        {
            for (int x = 0; x < size; x++)
            {
                bool inside = true;
                int cornerSize = Mathf.Clamp(corner, 1, size / 2);

                if (x < cornerSize && y < cornerSize)
                {
                    float dx = cornerSize - x;
                    float dy = cornerSize - y;
                    inside = Mathf.Sqrt(dx * dx + dy * dy) <= cornerSize;
                }
                else if (x >= size - cornerSize && y < cornerSize)
                {
                    float dx = x - (size - cornerSize - 1);
                    float dy = cornerSize - y;
                    inside = Mathf.Sqrt(dx * dx + dy * dy) <= cornerSize;
                }
                else if (x < cornerSize && y >= size - cornerSize)
                {
                    float dx = cornerSize - x;
                    float dy = y - (size - cornerSize - 1);
                    inside = Mathf.Sqrt(dx * dx + dy * dy) <= cornerSize;
                }
                else if (x >= size - cornerSize && y >= size - cornerSize)
                {
                    float dx = x - (size - cornerSize - 1);
                    float dy = y - (size - cornerSize - 1);
                    inside = Mathf.Sqrt(dx * dx + dy * dy) <= cornerSize;
                }

                tex.SetPixel(x, y, inside ? fill : clear);
            }
        }
        tex.Apply(false, true);
        Sprite sprite = Sprite.Create(tex, new Rect(0f, 0f, size, size), new Vector2(0.5f, 0.5f), 100f, 0, SpriteMeshType.FullRect, new Vector4(corner, corner, corner, corner));
        return sprite;
    }
